package assignment2;

public class Student {
	
	private int stuID;
	private String name;
	private String email;
	private double math_m;
	private double sc_m;

	public int getStuID() {
		return stuID;
	}
	public void setStuID(int stuID) {
		this.stuID = stuID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

	public double getMath_m() {
		return math_m;
	}
	public void setMath_m(double math_m) {
		this.math_m = math_m;
	}
	public double getSc_m() {
		return sc_m;
	}
	public void setSc_m(double sc_m) {
		this.sc_m = sc_m;
	}
}

